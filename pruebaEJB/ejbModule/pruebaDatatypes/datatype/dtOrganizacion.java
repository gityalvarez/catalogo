package pruebaDatatypes.datatype;

public class dtOrganizacion {
	private int id;
	private String nombre;
	public dtOrganizacion(int id, String nombre) {
		super();
		this.id = id;
		this.nombre = nombre;
	}
	public int getId() {
		return id;
	}
	public String getNombre() {
		return nombre;
	}
	
}
